package App.model;

public class TypeOperation {

	private int idType;
	private String type;
	
	
	
	public TypeOperation() {
		super();
	}



	public TypeOperation(int idType, String type) {
		super();
		this.idType = idType;
		this.type = type;
	}



	public int getIdType() {
		return idType;
	}



	public void setIdType(int idType) {
		this.idType = idType;
	}



	public String getType() {
		return type;
	}



	public void setType(String type) {
		this.type = type;
	}
	
	
	
	
	
	
	
	
}
